    function applyLang(lang) {
        document.querySelectorAll('[data-i18n]').forEach(function (el) {
            var key = el.getAttribute('data-i18n');
            if (lang === 'es') {
                if (!el.hasAttribute('data-orig')) {
                    el.setAttribute('data-orig', el.innerHTML);
                }
                if (ES[key] !== undefined) {
                    el.innerHTML = ES[key];
                }
            } else {
                if (el.hasAttribute('data-orig')) {
                    el.innerHTML = el.getAttribute('data-orig');
                }
            }
        });
        document.querySelectorAll('.lang-label').forEach(function (lbl) {
            lbl.textContent = lang === 'es' ? 'EN' : 'ES';
        });
        try { localStorage.setItem('ct-lang', lang); } catch (e) {}
        document.documentElement.lang = lang === 'es' ? 'es' : 'en';
    }

    window.ctToggleLang = function () {
        var current = 'en';
        try { current = localStorage.getItem('ct-lang') || 'en'; } catch (e) {}
        applyLang(current === 'en' ? 'es' : 'en');
    };

    /* Apply saved preference on every page load */
    document.addEventListener('DOMContentLoaded', function () {
        var saved = 'en';
        try { saved = localStorage.getItem('ct-lang') || 'en'; } catch (e) {}
        if (saved === 'es') applyLang('es');
    });
